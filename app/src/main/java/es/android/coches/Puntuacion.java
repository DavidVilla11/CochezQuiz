package es.android.coches;

public class Puntuacion {

    private int Puntuacion_maxima;
    private int Ultima_puntuacion;

    public Puntuacion() {
    }

    public int getPuntuacion_maxima() {
        return Puntuacion_maxima;
    }

    public void setPuntuacion_maxima(int puntuacion_maxima) {
        Puntuacion_maxima = puntuacion_maxima;
    }

    public int getUltima_puntuacion() {
        return Ultima_puntuacion;
    }

    public void setUltima_puntuacion(int ultima_puntuacion) {
        Ultima_puntuacion = ultima_puntuacion;
    }
}
